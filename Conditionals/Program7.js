// 7. Create a switch statement to return the name of a month based on its number (1 for January, 2 for February, etc.).
let monthNumber = 3;
let monthName;
switch (monthNumber) {
 
    case 1:
    monthName = "January";
    break;  
    case 2:
    monthName = "February";
    break;
case 3:
    monthName = "March";
    break;
case 4:
    monthName = "April";
    break;
case 5:
    monthName = "May";
    break;
case 6:
    monthName = "June";
    break;
case 7:
    monthName = "July";
    break;
case 8:
    monthName = "August";
    break;
case 9:
    monthName = "September";
    break;
case 10:
    monthName = "October";
    break;
case 11:
    monthName = "November";
    break;
case 12:
    monthName = "December";
    break;
default:
    monthName = "Invalid month number";
}
console.log(monthName);


